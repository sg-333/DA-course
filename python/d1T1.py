# SIMPLE CALCULATOR APP by assigning values

# Assigning two values 
num1 = 10
num2 = 2

# Performing arithmetic operations
add = num1 + num2
sub = num1 - num2
multiply = num1 * num2
divide = num1 / num2
floorD = num1 // num2
mod = num1 % num2
exp = num1 ** num2

print("Addition: ", add)
print("Subtraction: ", sub)
print("Multiplication: ", multiply) 
print("Division: ", divide)
print("Floor Division: ", floorD)
print("Modulus: ", mod)
print("Exponentiation: ", exp)